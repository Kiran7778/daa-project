public class QuickSort {

    // Main quicksort logic
    public static void quickSort(int[] arr, int low, int high) {
        if (low < high) {
            int pivotIndex = partition(arr, low, high);
            quickSort(arr, low, pivotIndex - 1);
            quickSort(arr, pivotIndex + 1, high);
        }
    }

    // Partition logic
    private static int partition(int[] arr, int low, int high) {
        int pivot = arr[high];
        int i = low - 1;

        for (int j = low; j < high; j++) {
            if (arr[j] < pivot) {
                i++;
                swap(arr, i, j);
            }
        }

        swap(arr, i + 1, high);
        return i + 1;
    }

    // Swap two elements in array
    private static void swap(int[] arr, int i, int j) {
        int temp = arr[i];
        arr[i] = arr[j];
        arr[j] = temp;
    }

    // Measure time taken by quickSort
    public static long measureTime(int[] arr) {
        int[] copy = arr.clone();  // Work on a copy so original remains unchanged
        long start = System.nanoTime();
        quickSort(copy, 0, copy.length - 1);
        long end = System.nanoTime();
        return (end - start) / 1_000_000; // return time in milliseconds
    }

    // Optional: method to print the array
    public static void printArray(int[] arr) {
        for (int num : arr) {
            System.out.print(num + " ");
        }
        System.out.println();
    }
}
